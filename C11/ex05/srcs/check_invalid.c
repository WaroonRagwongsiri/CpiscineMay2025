int	check_operator_invalid(char *str)
{
	if ((str[0] != '+' && str[0] != '-'
			&& str[0] != '*' && str[0] != '/'
			&& str[0] != '%') || (str[1] != 0))
	{
		return (1);
	}
	return (0);
}
